-------------------------------------------------------------------------------
-- 搜索设置
-------------------------------------------------------------------------------

-- 边输入搜索词边匹配（增量搜索）
vim.opt.incsearch = true
-- 搜索时默认忽略大小写
vim.opt.ignorecase = true
-- 如果搜索词中包含大写字母，则自动变为大小写敏感
vim.opt.smartcase = true

-------------------------------------------------------------------------------
-- 行号与光标
-------------------------------------------------------------------------------

-- 显示行号
vim.opt.number = true
-- 显示相对行号
-- vim.opt.relativenumber = true
-- 显示光标当前位置
vim.opt.ruler = true
-- 光标行高亮
-- vim.opt.cursorline = true
-- 设置光标样式
vim.opt.guicursor = "n-v-c:block,i-ci-ve:ver25,r-cr:hor20,o:hor50"

-------------------------------------------------------------------------------
-- 制表符与缩进
-------------------------------------------------------------------------------

-- 制表符显示的宽度
vim.opt.tabstop = 8
-- 使用 >> 或 << 进行缩进/反缩进时，移动 8 个空格
vim.opt.shiftwidth = 8
-- 将制表符转换为空格 (默认注释，需要时取消注释)
-- vim.opt.expandtab = true
-- 自动缩进
vim.opt.autoindent = true

-------------------------------------------------------------------------------
-- 分割方向
-------------------------------------------------------------------------------

-- 设置拆分默认为向下
-- bel -- 下
-- top -- 上
vim.opt.splitbelow = true
-- 设置垂直拆分时默认向右
vim.opt.splitright = true

-------------------------------------------------------------------------------
-- 状态行
-------------------------------------------------------------------------------

-- 显示当前模式 (Neovim 默认已显示，但显式开启无妨)
vim.opt.showmode = true
-- 显示输入的命令
vim.opt.showcmd = true
-- 总是显示状态栏
vim.opt.laststatus = 2
-- 设置状态行显示内容
vim.opt.statusline = "%#StatusLineTerm# %<%.20f%m%r %#StatusLineNC# [%{&ff}] [%Y] %= %l:%c %p%% %{strftime('%R')}"

-------------------------------------------------------------------------------
-- 系统与GUI
-------------------------------------------------------------------------------

-- 启用鼠标支持 (可选择、滚动等)
vim.opt.mouse = "a"
-- 使用系统剪贴板（方便复制粘贴到系统外部）
vim.opt.clipboard = 'unnamedplus'
-- 补全菜单选项：显示菜单、仅有一个结果也显示菜单、默认不选中任何项
vim.opt.completeopt = { 'menu', 'menuone', 'noselect' }
-- 修复 Insert 模式下退格键失效问题
vim.opt.backspace = "indent,eol,start"

-------------------------------------------------------------------------------
-- 其他
-------------------------------------------------------------------------------

-- 取消备份文件
vim.opt.backup = false
vim.opt.writebackup = false
vim.opt.swapfile = false

-- 关闭提示音
vim.opt.belloff = "all"


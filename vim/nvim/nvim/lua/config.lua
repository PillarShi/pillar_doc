-- vim.cmd.colorscheme("industry")
-- vim.cmd.colorscheme("pablo")
-- vim.cmd.colorscheme("slate")
-- 语法高亮
-- vim.cmd("syntax on")

-- Windows 配置
if vim.fn.has("win32") == 1 then
  vim.opt.shell = "powershell.exe"
end

